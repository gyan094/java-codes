public class Conditional_statements {
    public static void main(String[] args) {
        //conditional stt
        boolean isSunUp =true;
        if(isSunUp == true)
            System.out.println("day");
        else
            System.out.println("night");  //day



        boolean isSunUps =false;
        if(isSunUps == true)
            System.out.println("day");
        else
            System.out.println("night");  //night


    }
}
